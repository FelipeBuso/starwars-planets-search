import React, { useContext, useEffect } from 'react';
import StarwarsPlanetsContext from '../context/StarwarsPlanetsContext';

function Table() {
  const { data, isLoading, filters } = useContext(StarwarsPlanetsContext);
  const { name } = filters.filters.filterByName;
  const filterPlanet = data.filter((item) => item.name.toLowerCase()
    .includes(name.toLocaleLowerCase()));

  useEffect(() => {

  }, [name]);

  return !isLoading ? (
    <table>
      <thead>
        <tr>
          <th>Name</th>
          <th>Rotation Period</th>
          <th>Orbital Period</th>
          <th>Diameter</th>
          <th>Climate</th>
          <th>Gravity</th>
          <th>Terrain</th>
          <th>Surface Water</th>
          <th>Population</th>
          <th>Films</th>
          <th>Created</th>
          <th>Edited</th>
          <th>URL</th>
        </tr>
      </thead>
      <tbody>
        {filterPlanet.map((planet, index) => (
          <tr key={ index }>
            <td>{ planet.name }</td>
            <td>{ planet.rotation_period }</td>
            <td>{ planet.orbital_period }</td>
            <td>{ planet.diameter }</td>
            <td>{ planet.climate }</td>
            <td>{ planet.gravity }</td>
            <td>{ planet.terrain }</td>
            <td>{ planet.surface_water }</td>
            <td>{ planet.population }</td>
            <td>{ planet.films[0] }</td>
            <td>{ planet.created }</td>
            <td>{ planet.edited }</td>
            <td>{ planet.url }</td>
          </tr>
        ))}
      </tbody>
    </table>
  ) : <span>Carregando...</span>;
}

export default Table;
